#include "libft.h"

int	ft_words(char *s, char c)
{
	int	i;
	int	w;

	i = 0;
	w = 0;
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i] != '\0')
			w++;
		while (s[i] != c && s[i])
			i++;
	}
	return (w);
}
