#include "libft.h"

char	*ft_strcat(char *d, const char *a)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (d[i])
		i++;
	while (a[j])
	{
		d[i + j] = a[j];
		j++;
	}
	d[i + j] = '\0';
	return (d);
}
