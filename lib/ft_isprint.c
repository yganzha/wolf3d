#include "libft.h"

int	ft_isprint(int a)
{
	if ((a > 31 && a < 48) || (a >= '0' && a <= '9') ||
		(a > 57 && a < 65) || (a >= 'A' && a <= 'Z') ||
		(a > 90 && a < 97) || (a >= 'a' && a <= 'z') ||
		(a > 122 && a < 127))
		return (1);
	return (0);
}
