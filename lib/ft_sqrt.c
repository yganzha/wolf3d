#include "libft.h"

int		ft_sqrt(int nb)
{
	int		a;
	int		b;

	a = 1;
	b = 0;
	while (nb > 0)
	{
		nb = nb - a;
		a = a + 2;
		b++;
	}
	if (nb == 0)
		return (b);
	else
		return (0);
}
